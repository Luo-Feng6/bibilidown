function showAndFocusWindow() {
  if (!mainWindow) return
  if (mainWindow.isMinimized()) mainWindow.restore()
  if (!mainWindow.isVisible()) mainWindow.show()
  mainWindow.focus()
}

function updateTrayMenu(downloadStats) {
  if (!tray) return

  const activeCount = downloadStats?.active ?? 0
  const completedCount = downloadStats?.completed ?? 0
  const totalCount = downloadStats?.total ?? 0

  let statusLabel = '下载队列: 无任务'
  if (totalCount > 0) {
    statusLabel = `下载: ${completedCount}/${totalCount} 完成`
    if (activeCount > 0) statusLabel += ` (${activeCount} 进行中)`
  }

  const contextMenu = Menu.buildFromTemplate([
    {
      label: '显示窗口',
      click: () => showAndFocusWindow(),
    },
    {
      label: statusLabel,
      enabled: false,
    },
    { type: 'separator' },
    {
      label: '退出 BilibiliDown',
      click: () => {
        isQuitting = true
        app.quit()
      },
    },
  ])

  tray.setContextMenu(contextMenu)
}

/* ── IPC: tray notifications ── */

ipcMain.handle('tray:updateStats', (_event, stats) => {
  updateTrayMenu(stats)

  // Update tooltip with download info
  if (tray && stats && stats.total > 0) {
    const tooltip = stats.active > 0
      ? `BilibiliDown — 下载中 ${stats.completed}/${stats.total}`
      : `BilibiliDown — ${stats.completed}/${stats.total} 已完成`
    tray.setToolTip(tooltip)
  } else if (tray) {
    tray.setToolTip('BilibiliDown')
  }
})

