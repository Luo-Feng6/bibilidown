/**
 * Download a file from a URL and save it to disk.
 *
 * Input: { url, filePath, headers?, referer? }
 * Output: { success, filePath, size, error? }
 *
 * Uses Node.js http/https for direct streaming — no need to pass
 * large blobs through IPC. Reports progress back via ipc event.
 */
ipcMain.handle('download:saveToDisk', async (_event, { url, filePath, headers, referer }) => {
  return new Promise((resolve) => {
    try {
      // Parse the URL to determine http vs https
      const parsedUrl = new URL(url)
      const mod = parsedUrl.protocol === 'https:' ? https : http

      const requestHeaders = {
        Referer: referer || 'https://www.bilibili.com',
        Origin: 'https://www.bilibili.com',
        'User-Agent':
          'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
        ...(headers || {}),
      }

      const req = mod.request(
        url,
        {
          method: 'GET',
          headers: requestHeaders,
        },
        (res) => {
          // Handle redirects
          if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
            // Follow redirect
            const redirectUrl = new URL(res.headers.location, url).toString()
            // Recursively call — use the same handler
            const subReq = (redirectUrl.startsWith('https:') ? https : http).request(
              redirectUrl,
              { method: 'GET', headers: requestHeaders },
              (redirectRes) => {
                streamToFile(redirectRes, filePath, resolve)
              }
            )
            subReq.on('error', (err) => resolve({ success: false, error: err.message }))
            subReq.end()
            return
          }

          if (res.statusCode !== 200) {
            resolve({ success: false, error: `HTTP ${res.statusCode}: ${res.statusMessage}` })
            return
          }

          streamToFile(res, filePath, resolve)
        }
      )

      req.on('error', (err) => {
        resolve({ success: false, error: err.message })
      })

      req.setTimeout(300000, () => {
        req.destroy()
        resolve({ success: false, error: '下载超时' })
      })

      req.end()
    } catch (err) {
      resolve({ success: false, error: err.message })
    }
  })
})

/**
 * Stream an HTTP response to a file, with progress reporting.
 */
function streamToFile(response, filePath, resolve) {
  const totalSize = parseInt(response.headers['content-length'] || '0', 10)
  let downloadedSize = 0
  let lastReportTime = Date.now()
  let lastReportSize = 0

  // Ensure directory exists
  const dir = path.dirname(filePath)
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true })
  }

  const writeStream = fs.createWriteStream(filePath)

  response.on('data', (chunk) => {
    downloadedSize += chunk.length
    writeStream.write(chunk)

    // Report progress every 200ms
    const now = Date.now()
    if (now - lastReportTime >= 200) {
      const speed = (downloadedSize - lastReportSize) / ((now - lastReportTime) / 1000)
      lastReportSize = downloadedSize
      lastReportTime = now

      if (mainWindow && !mainWindow.isDestroyed()) {
        mainWindow.webContents.send('download:progress', {
          filePath,
          downloadedSize,
          totalSize,
          speed,
        })
      }
    }
  })

  response.on('end', () => {
    writeStream.end()
    resolve({
      success: true,
      filePath,
      size: downloadedSize,
    })
  })

  response.on('error', (err) => {
    writeStream.close()
    // Clean up partial file
    try { fs.unlinkSync(filePath) } catch {}
    resolve({ success: false, error: err.message })
  })

  writeStream.on('error', (err) => {
    response.destroy()
    try { fs.unlinkSync(filePath) } catch {}
    resolve({ success: false, error: err.message })
  })
}

/* ── IPC handlers: FFmpeg merge ── */

/**
 * Merge video + audio streams into a single MP4 file using FFmpeg.
 *
 * Input: { videoPath, audioPath, outputPath }
 * Output: { success, outputPath, error? }
 */
ipcMain.handle('ffmpeg:merge', async (_event, { videoPath, audioPath, outputPath }) => {
  return new Promise((resolve) => {
    // Find ffmpeg — check common locations
    const ffmpegPath = findFfmpeg()

    const args = [
      '-i', videoPath,
      '-i', audioPath,
      '-c:v', 'copy',       // Copy video codec (fast, no re-encode)
      '-c:a', 'aac',         // Re-encode audio to AAC (compatible with MP4)
      '-b:a', '192k',        // Audio bitrate
      '-movflags', '+faststart', // Web-optimized
      '-y',                   // Overwrite output
      outputPath,
    ]

    console.log(`[FFmpeg] ${ffmpegPath} ${args.join(' ')}`)

    const ffmpeg = spawn(ffmpegPath, args, {
      stdio: ['pipe', 'pipe', 'pipe'],
    })

    let stderr = ''

    ffmpeg.stderr.on('data', (data) => {
      stderr += data.toString()
    })

    ffmpeg.on('close', (code) => {
      if (code === 0) {
        // Clean up temp files
        try { fs.unlinkSync(videoPath) } catch {}
        try { fs.unlinkSync(audioPath) } catch {}
        resolve({ success: true, outputPath })
      } else {
        console.error(`[FFmpeg] exit code ${code}: ${stderr}`)
        resolve({ success: false, error: `FFmpeg 合并失败 (exit ${code})` })
      }
    })

    ffmpeg.on('error', (err) => {
      console.error(`[FFmpeg] spawn error:`, err)
      resolve({ success: false, error: `无法启动 FFmpeg: ${err.message}` })
    })
  })
})

/**
 * Check if FFmpeg is available on the system.
 */
ipcMain.handle('ffmpeg:check', async () => {
  const found = findFfmpeg()
  if (!found) return { available: false, path: '' }

  // Test that it actually works
  return new Promise((resolve) => {
    const ffmpeg = spawn(found, ['-version'], { stdio: 'pipe' })
    ffmpeg.on('close', (code) => {
      resolve({ available: code === 0, path: found })
    })
    ffmpeg.on('error', () => {
      resolve({ available: false, path: '' })
    })
  })
})

/**
 * Find ffmpeg executable on the system.
 * Checks: bundled ffmpeg, PATH, common install locations.
 */
function findFfmpeg() {
  // 1. Check bundled ffmpeg (in app resources)
  const bundledPath = path.join(
    process.resourcesPath || app.getAppPath(),
    'ffmpeg',
    process.platform === 'win32' ? 'ffmpeg.exe' : 'ffmpeg'
  )
  if (fs.existsSync(bundledPath)) return bundledPath

  // 2. Check same directory as the app
  const appDir = path.dirname(app.getPath('exe'))
  const appDirFfmpeg = path.join(appDir, process.platform === 'win32' ? 'ffmpeg.exe' : 'ffmpeg')
  if (fs.existsSync(appDirFfmpeg)) return appDirFfmpeg

  // 3. Check common Windows locations
  if (process.platform === 'win32') {
    const commonPaths = [
      'C:\\ffmpeg\\bin\\ffmpeg.exe',
      'C:\\Program Files\\ffmpeg\\bin\\ffmpeg.exe',
      path.join(process.env.LOCALAPPDATA || '', 'ffmpeg', 'ffmpeg.exe'),
      path.join(process.resourcesPath || '', 'ffmpeg', 'ffmpeg.exe'),
    ]
    for (const p of commonPaths) {
      if (fs.existsSync(p)) return p
    }
  }

  // 4. Fall back to PATH
  return process.platform === 'win32' ? 'ffmpeg.exe' : 'ffmpeg'
}

