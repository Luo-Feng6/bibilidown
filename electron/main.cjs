/* ── IPC handlers: file operations ── */

/**
 * Move/rename a file from one path to another.
 */
ipcMain.handle('file:move', async (_event, { from, to }) => {
  try {
    // Ensure target directory exists
    const targetDir = path.dirname(to)
    if (!fs.existsSync(targetDir)) {
      fs.mkdirSync(targetDir, { recursive: true })
    }
    fs.renameSync(from, to)
    return { success: true }
  } catch (err) {
    // If rename fails (e.g., across drives), try copy + delete
    try {
      fs.copyFileSync(from, to)
      fs.unlinkSync(from)
      return { success: true }
    } catch (copyErr) {
      return { success: false, error: copyErr.message }
    }
  }
})

